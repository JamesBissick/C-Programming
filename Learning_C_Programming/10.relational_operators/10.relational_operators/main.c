#include <stdio.h>

int main(int argc, const char * argv[]) {
    /* Relational operators
     *      >
     *      <
     *      >=
     *      <=
     *      ==
     *      !=
     *
     *      Booleans :
     *          1 = true
     *          0 = false
     */
    int a, b, i;
    a = 100;
    b = 150;
    i = a != b;
    printf("%d\n", i); // (a is not equal to b) = true = (1)
    
}
